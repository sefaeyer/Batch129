package ssg12;

public class ForLoop02 {

    public static void main(String[] args) {

        //soru 2)5 hariç 3'ten 9'a kadar olan tam sayıları yazdırmak için kod yazınız.

        for(int i = 3; i<=9; i++){

            if(i==5){
                continue;

            }else System.out.println(i);



        }



    }
}
